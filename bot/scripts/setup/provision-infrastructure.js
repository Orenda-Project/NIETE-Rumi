#!/usr/bin/env node

/**
 * bd-345: Setup Agent Integration - Provision Infrastructure
 *
 * This script calls the Rumi Provisioner API to automatically create
 * Supabase and Railway projects for a new deployment.
 *
 * Usage:
 *   node provision-infrastructure.js --name my-org-name
 *   node provision-infrastructure.js --name my-org-name --region eu-west-1
 */

const fs = require('fs');
const path = require('path');

// Provisioner configuration
const PROVISIONER_URL = process.env.PROVISIONER_URL || 'https://rumi-provisioner.up.railway.app';
const PROVISIONER_KEY = process.env.PROVISIONER_KEY || 'rumi-prov-2026-trial';

/**
 * Call the provisioner API to create infrastructure
 */
async function provisionInfrastructure(deploymentName, region = 'ap-south-1') {
  console.log(`\n🚀 Provisioning infrastructure for: ${deploymentName}`);
  console.log(`   Region: ${region}`);
  console.log(`   Provisioner: ${PROVISIONER_URL}\n`);

  const response = await fetch(`${PROVISIONER_URL}/provision`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'X-Provisioner-Key': PROVISIONER_KEY
    },
    body: JSON.stringify({
      deployment_name: deploymentName,
      region: region,
      tier: 'recommended'
    })
  });

  const result = await response.json();

  if (!result.success) {
    throw new Error(`Provisioning failed: ${result.message}`);
  }

  return result;
}

/**
 * Write credentials to .env file
 */
function writeEnvFile(credentials, envPath) {
  const envContent = `# Auto-generated by provision-infrastructure.js
# Generated at: ${new Date().toISOString()}

# Supabase Configuration
SUPABASE_URL=${credentials.supabase.url}
SUPABASE_ANON_KEY=${credentials.supabase.anon_key}
SUPABASE_SERVICE_KEY=${credentials.supabase.service_key}

# Redis Configuration
REDIS_URL=${credentials.railway.redis_url}

# Railway Project
# Dashboard: ${credentials.railway.project_url}
# Deploy command: ${credentials.railway.deploy_command}

# ====================================
# REQUIRED: Add these manually
# ====================================

# WhatsApp Configuration (from Meta Business)
WHATSAPP_TOKEN=your_whatsapp_token_here
PHONE_NUMBER_ID=your_phone_number_id_here
WABA_ID=your_waba_id_here
WEBHOOK_VERIFY_TOKEN=your_random_verify_token_here

# AI Configuration
OPENROUTER_API_KEY=your_openrouter_api_key_here

# Internal API Key (generate a random string)
INTERNAL_API_KEY=${generateRandomKey()}

# Bot Configuration
BOT_NAME=${credentials.deployment_name}
ORG_NAME=${credentials.deployment_name}
NODE_ENV=production
`;

  fs.writeFileSync(envPath, envContent);
  console.log(`✅ Credentials written to ${envPath}`);
}

/**
 * Generate a random API key
 */
function generateRandomKey() {
  return 'rumi-' + Math.random().toString(36).substring(2, 15) +
    Math.random().toString(36).substring(2, 15);
}

/**
 * Parse command line arguments
 */
function parseArgs() {
  const args = process.argv.slice(2);
  const options = {
    name: null,
    region: 'ap-south-1'
  };

  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--name' && args[i + 1]) {
      options.name = args[i + 1];
      i++;
    } else if (args[i] === '--region' && args[i + 1]) {
      options.region = args[i + 1];
      i++;
    } else if (args[i] === '--help' || args[i] === '-h') {
      console.log(`
Usage: node provision-infrastructure.js --name <deployment-name> [--region <region>]

Options:
  --name    Required. Name for your deployment (e.g., "my-school")
  --region  Optional. Supabase region (default: ap-south-1)
            Options: us-east-1, us-west-1, eu-west-1, ap-south-1, ap-southeast-1

Examples:
  node provision-infrastructure.js --name acme-education
  node provision-infrastructure.js --name my-school --region eu-west-1
`);
      process.exit(0);
    }
  }

  return options;
}

/**
 * Main function
 */
async function main() {
  const options = parseArgs();

  if (!options.name) {
    console.error('❌ Error: --name is required');
    console.error('   Run with --help for usage information');
    process.exit(1);
  }

  try {
    // Provision infrastructure
    const credentials = await provisionInfrastructure(options.name, options.region);

    console.log('✅ Infrastructure provisioned successfully!\n');

    // Show summary
    console.log('📦 Supabase Project:');
    console.log(`   URL: ${credentials.supabase.url}`);
    console.log(`   Project ID: ${credentials.supabase.project_id}`);

    console.log('\n🚂 Railway Project:');
    console.log(`   Dashboard: ${credentials.railway.project_url}`);
    console.log(`   Deploy: ${credentials.railway.deploy_command}`);

    // Write .env file
    const envPath = path.join(process.cwd(), '.env');
    writeEnvFile(credentials, envPath);

    console.log('\n📋 Next Steps:');
    credentials.next_steps.forEach((step, i) => {
      console.log(`   ${i + 1}. ${step}`);
    });

    console.log('\n🎉 Done! Edit .env to add your WhatsApp and OpenRouter credentials.');

  } catch (error) {
    console.error(`\n❌ Provisioning failed: ${error.message}`);
    process.exit(1);
  }
}

// Run if called directly
if (require.main === module) {
  main();
}

// Export for testing
module.exports = {
  provisionInfrastructure,
  writeEnvFile,
  generateRandomKey,
  parseArgs
};
