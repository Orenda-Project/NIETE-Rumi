#!/usr/bin/env node

/**
 * bd-345: Setup Agent Integration - Provision Infrastructure
 *
 * This script calls the Rumi Provisioner API to automatically create
 * Supabase and Railway projects for a new deployment.
 *
 * Usage:
 *   node provision-infrastructure.js --name my-org-name
 *   node provision-infrastructure.js --name my-org-name --region eu-west-1
 */

const fs = require('fs');
const path = require('path');

// Provisioner configuration
const PROVISIONER_URL = process.env.PROVISIONER_URL || 'https://provisioner-api-production.up.railway.app';
const PROVISIONER_KEY = process.env.PROVISIONER_KEY;

/**
 * Call the provisioner API to create infrastructure
 */
async function provisionInfrastructure(deploymentName, region = 'ap-south-1', tier = 'recommended') {
  if (!PROVISIONER_KEY) {
    console.error('Error: PROVISIONER_KEY environment variable is required.');
    console.error('Set it in your .env file or export it before running this script.');
    process.exit(1);
  }

  console.log(`\n🚀 Provisioning infrastructure for: ${deploymentName}`);
  console.log(`   Region: ${region}`);
  console.log(`   Tier: ${tier}`);
  console.log(`   Provisioner: ${PROVISIONER_URL}\n`);

  const response = await fetch(`${PROVISIONER_URL}/provision`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'X-Provisioner-Key': PROVISIONER_KEY
    },
    body: JSON.stringify({
      deployment_name: deploymentName,
      region: region,
      tier: tier
    })
  });

  const result = await response.json();

  if (!result.success) {
    throw new Error(`Provisioning failed: ${result.message}`);
  }

  return result;
}

/**
 * Write credentials to .env file
 */
function writeEnvFile(credentials, envPath) {
  // Check what services were provisioned
  const hasOpenRouter = credentials.openrouter && credentials.openrouter.api_key;
  const hasSoniox = credentials.soniox && credentials.soniox.api_key;
  const hasElevenLabs = credentials.elevenlabs && credentials.elevenlabs.api_key;
  const hasAzureSpeech = credentials.azure_speech && credentials.azure_speech.key;
  const tier = credentials.tier || 'recommended';

  // bd-383: Preserve existing WhatsApp credentials from .env
  const whatsappKeys = ['WHATSAPP_TOKEN', 'PHONE_NUMBER_ID', 'WABA_ID', 'WEBHOOK_VERIFY_TOKEN'];
  const existingWhatsApp = {};
  if (fs.existsSync(envPath)) {
    const existing = fs.readFileSync(envPath, 'utf-8');
    for (const key of whatsappKeys) {
      const match = existing.match(new RegExp(`^${key}=(.+)$`, 'm'));
      if (match && !match[1].includes('your_') && !match[1].includes('_here')) {
        existingWhatsApp[key] = match[1];
      }
    }
  }

  let envContent = `# Auto-generated by provision-infrastructure.js
# Generated at: ${new Date().toISOString()}
# Tier: ${tier}

# ====================================
# TIER 1: Core Infrastructure
# ====================================

# Supabase Configuration
SUPABASE_URL=${credentials.supabase.url}
SUPABASE_ANON_KEY=${credentials.supabase.anon_key}
SUPABASE_SERVICE_ROLE_KEY=${credentials.supabase.service_key}
${credentials.supabase.db_connection ? `DATABASE_URL=${credentials.supabase.db_connection.connection_string}` : '# DATABASE_URL=postgresql://postgres:PASSWORD@db.PROJECT_REF.supabase.co:5432/postgres'}

# Redis Configuration
REDIS_URL=${credentials.railway.redis_url}

# Railway Project
# Dashboard: ${credentials.railway.project_url}
${credentials.railway.deploy_token ? `RAILWAY_DEPLOY_TOKEN=${credentials.railway.deploy_token.token}
# Deploy command: ${credentials.railway.deploy_token.usage}` : '# Deploy: See SETUP.md Step 5'}

# AI Configuration (OpenRouter)
${hasOpenRouter
    ? `# Auto-provisioned: ${credentials.openrouter.limit}, expires ${credentials.openrouter.expires_at}
OPENROUTER_API_KEY=${credentials.openrouter.api_key}`
    : `# REQUIRED: Add your OpenRouter API key
OPENROUTER_API_KEY=your_openrouter_api_key_here`}
LLM_PROVIDER=openrouter
`;

  // Add Tier 2 services (Soniox for STT)
  if (tier === 'recommended' || tier === 'full') {
    envContent += `
# ====================================
# TIER 2: Speech-to-Text
# ====================================

${hasSoniox
    ? `# Auto-provisioned temp key (expires ${credentials.soniox.expires_at})
# ${credentials.soniox.note}
SONIOX_API_KEY=${credentials.soniox.api_key}`
    : `# REQUIRED for Tier 2+: Add Soniox API key
SONIOX_API_KEY=your_soniox_api_key_here`}
`;
  }

  // Add Tier 3 services (ElevenLabs TTS + Azure Speech)
  if (tier === 'full') {
    envContent += `
# ====================================
# TIER 3: Text-to-Speech & Pronunciation
# ====================================

${hasElevenLabs
    ? `# Shared ElevenLabs key (${credentials.elevenlabs.type})
# ${credentials.elevenlabs.note}
ELEVENLABS_API_KEY=${credentials.elevenlabs.api_key}`
    : `# OPTIONAL for Tier 3: Add ElevenLabs API key
ELEVENLABS_API_KEY=your_elevenlabs_api_key_here`}

${hasAzureSpeech
    ? `# Shared Azure Speech key (${credentials.azure_speech.type})
# ${credentials.azure_speech.note}
AZURE_SPEECH_KEY=${credentials.azure_speech.key}
AZURE_SPEECH_REGION=${credentials.azure_speech.region}`
    : `# OPTIONAL for Tier 3: Add Azure Speech credentials
AZURE_SPEECH_KEY=your_azure_speech_key_here
AZURE_SPEECH_REGION=eastus`}
`;
  }

  envContent += `
# ====================================
# REQUIRED: Add these manually
# ====================================

# WhatsApp Configuration (from Meta Business)
WHATSAPP_TOKEN=${existingWhatsApp.WHATSAPP_TOKEN || 'your_whatsapp_token_here'}
PHONE_NUMBER_ID=${existingWhatsApp.PHONE_NUMBER_ID || 'your_phone_number_id_here'}
WABA_ID=${existingWhatsApp.WABA_ID || 'your_waba_id_here'}
WEBHOOK_VERIFY_TOKEN=${existingWhatsApp.WEBHOOK_VERIFY_TOKEN || 'your_random_verify_token_here'}

# Internal API Key (generate a random string)
INTERNAL_API_KEY=${generateRandomKey()}

# Bot Configuration
BOT_NAME=${credentials.deployment_name}
ORG_NAME=${credentials.deployment_name}
NODE_ENV=production
RUMI_TIER=${tier}
`;

  fs.writeFileSync(envPath, envContent);
  console.log(`✅ Credentials written to ${envPath}`);
}

/**
 * Generate a random API key
 */
function generateRandomKey() {
  return 'rumi-' + Math.random().toString(36).substring(2, 15) +
    Math.random().toString(36).substring(2, 15);
}

/**
 * Parse command line arguments
 */
function parseArgs() {
  const args = process.argv.slice(2);
  const options = {
    name: null,
    region: 'ap-south-1',
    tier: 'recommended'
  };

  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--name' && args[i + 1]) {
      options.name = args[i + 1];
      i++;
    } else if (args[i] === '--region' && args[i + 1]) {
      options.region = args[i + 1];
      i++;
    } else if (args[i] === '--tier' && args[i + 1]) {
      const tier = args[i + 1].toLowerCase();
      if (!['minimal', 'recommended', 'full'].includes(tier)) {
        console.error(`❌ Invalid tier: ${tier}. Must be minimal, recommended, or full.`);
        process.exit(1);
      }
      options.tier = tier;
      i++;
    } else if (args[i] === '--help' || args[i] === '-h') {
      console.log(`
Usage: node provision-infrastructure.js --name <deployment-name> [--tier <tier>] [--region <region>]

Options:
  --name    Required. Name for your deployment (e.g., "my-school")
  --tier    Optional. Feature tier (default: recommended)
            minimal     - AI Chat + Registration (1 API: OpenRouter)
            recommended - + Coaching + Reading Assessment (+ Soniox STT)
            full        - All features (+ ElevenLabs TTS, Azure Speech)
  --region  Optional. Supabase region (default: ap-south-1)
            Options: us-east-1, us-west-1, eu-west-1, ap-south-1, ap-southeast-1

Examples:
  node provision-infrastructure.js --name acme-education
  node provision-infrastructure.js --name my-school --tier full
  node provision-infrastructure.js --name my-school --tier minimal --region eu-west-1
`);
      process.exit(0);
    }
  }

  return options;
}

/**
 * Main function
 */
async function main() {
  const options = parseArgs();

  if (!options.name) {
    console.error('❌ Error: --name is required');
    console.error('   Run with --help for usage information');
    process.exit(1);
  }

  try {
    // Provision infrastructure
    const credentials = await provisionInfrastructure(options.name, options.region, options.tier);

    console.log('✅ Infrastructure provisioned successfully!\n');

    // Show summary
    console.log('📦 Supabase Project:');
    console.log(`   URL: ${credentials.supabase.url}`);
    console.log(`   Project ID: ${credentials.supabase.project_id}`);
    if (credentials.supabase.dashboard_url) {
      console.log(`   Dashboard: ${credentials.supabase.dashboard_url}`);
    }
    if (credentials.supabase.db_connection) {
      console.log(`   DB Host: ${credentials.supabase.db_connection.host}`);
      console.log(`   DB Connection: ${credentials.supabase.db_connection.connection_string}`);
    }
    if (credentials.supabase.migrations) {
      const migrationError = credentials.supabase.migrations.error;
      if (migrationError) {
        console.log(`   ⚠️  Migrations failed: ${migrationError}`);
        console.log('   Run manually: See SETUP.md Step 4');
      } else {
        console.log('   ✅ Database schema + seed data applied');
      }
    }

    console.log('\n🚂 Railway Project:');
    console.log(`   Dashboard: ${credentials.railway.project_url}`);
    console.log(`   Deploy: ${credentials.railway.deploy_token?.usage || 'cd bot && railway up --service bot'}`);

    // Show OpenRouter info if provisioned
    if (credentials.openrouter) {
      console.log('\n🤖 OpenRouter API Key:');
      console.log(`   Limit: ${credentials.openrouter.limit}`);
      console.log(`   Expires: ${credentials.openrouter.expires_at}`);
      console.log('   ✅ Auto-configured in .env');
    }

    // Show Soniox info if provisioned (Tier 2+)
    if (credentials.soniox) {
      console.log('\n🎤 Soniox STT:');
      console.log(`   Expires: ${credentials.soniox.expires_at}`);
      console.log('   ✅ Auto-configured in .env');
    }

    // Show ElevenLabs info if provisioned (Tier 3)
    if (credentials.elevenlabs) {
      console.log('\n🔊 ElevenLabs TTS:');
      console.log(`   Type: ${credentials.elevenlabs.type}`);
      console.log('   ✅ Auto-configured in .env');
    }

    // Show Azure Speech info if provisioned (Tier 3)
    if (credentials.azure_speech) {
      console.log('\n🗣️ Azure Speech:');
      console.log(`   Region: ${credentials.azure_speech.region}`);
      console.log(`   Type: ${credentials.azure_speech.type}`);
      console.log('   ✅ Auto-configured in .env');
    }

    // Write .env file
    const envPath = path.join(process.cwd(), '.env');
    writeEnvFile(credentials, envPath);

    console.log('\n📋 Next Steps:');
    credentials.next_steps.forEach((step, i) => {
      console.log(`   ${i + 1}. ${step}`);
    });

    // Adjust final message based on what's still needed
    console.log('\n🎉 Done! Edit .env to add your WhatsApp credentials, then run: railway up');

  } catch (error) {
    console.error(`\n❌ Provisioning failed: ${error.message}`);
    process.exit(1);
  }
}

// Run if called directly
if (require.main === module) {
  main();
}

// Export for testing
module.exports = {
  provisionInfrastructure,
  writeEnvFile,
  generateRandomKey,
  parseArgs
};
